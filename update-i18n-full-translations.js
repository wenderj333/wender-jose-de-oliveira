function updateTranslationFile(lang, newKeys, type) {
  const path = `frontend/src/i18n/${lang}.json`;
  if (fs.existsSync(path)) {
    const data = JSON.parse(fs.readFileSync(path, 'utf8'));
    if (!data[type]) data[type] = {}; // Ensure the top-level key exists

    for (const [key, val] of Object.entries(newKeys)) {
      const nestedKeys = key.split('.');
      let current = data;
      for (let i = 0; i < nestedKeys.length; i++) {
        const nk = nestedKeys[i];
        if (i === nestedKeys.length - 1) {
          current[nk] = val; // Set the value at the deepest level
        } else {
          if (!current[nk] || typeof current[nk] !== 'object') {
            current[nk] = {}; // Create nested object if it doesn't exist or isn't an object
          }
          current = current[nk];
        }
      }
    }
    fs.writeFileSync(path, JSON.stringify(data, null, 2), 'utf8');
    console.log(`Updated ${lang}.json for ${type}`);
  } else {
    console.warn(`Translation file not found: ${path}`);
  }
}

for (const [lang, translations] of Object.entries(reflectionTranslations)) {
  updateTranslationFile(lang, translations, 'reflection');
}

for (const [lang, translations] of Object.entries(ajudaProximoTranslations)) {
  updateTranslationFile(lang, translations, 'ajudaProximo');
}
