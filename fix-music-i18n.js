const langs = Object.keys(translations);

for (const lang of langs) {
  const filePath = path.join(I18N_DIR, `${lang}.json`);
  if (!fs.existsSync(filePath)) {
    console.warn(`⚠️  ${lang}.json not found, skipping`);
    continue;
  }

  let json;
  try {
    json = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  } catch (e) {
    console.error(`❌ Failed to parse ${lang}.json:`, e.message);
    continue;
  }

  // Merge music keys (preserve existing if already present)
  const existing = json.music || {};
  json.music = { ...translations[lang], ...existing };

  fs.writeFileSync(filePath, JSON.stringify(json, null, 2), 'utf8');
  console.log(`✅ Updated ${lang}.json with music keys`);
}

console.log('\n🎵 All music i18n keys applied!');
